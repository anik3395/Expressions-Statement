public class WhileLoop {

    public static void main(String[] args) {
        int i ;
        for ( i =1 ; i <= 5; i++){
            System.out.println( i );
        }

        System.out.println();

        int j=1;
        while ( j <= 5){
            System.out.println(j);
            j++;
        }
    }
}
